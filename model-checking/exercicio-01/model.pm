	[] s=1 -> 2/3 : (s'=6) + 1/3 : (s'=1);
	[] s=2 -> 4/5 : (s'=6) + 1/5 : (s'=1);
	[] s=3 -> 8/9 : (s'=4) + 1/9 : (s'=3);
	[] s=4 -> 1 : (s'=3);
	[] s=5 -> 1/4 : (s'=6) + 1/4 : (s'=4) + 2/4 : (s'=0);
	[] s=6 -> 1/2 : (s'=2) + 1/2 : (s'=6);

endmodule
